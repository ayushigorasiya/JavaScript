document.write(`<h1>Program to find  rate of interest.</h1>`);

var p = parseInt(prompt("Enter The Element Of P :"));
var r = parseInt(prompt("Enter The Element Of R :"));
var n = parseInt(prompt("Enter The Element Of N :"));

var ans = (p * r * n / 100);

document.write(`<h2> :) The Rate Of Intrest is :- ${ans}</h2>`)
